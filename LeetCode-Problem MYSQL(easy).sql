--Problem 1251. Average Selling Price
SELECT P.PRODUCT_ID, 
    IFNULL(
        ROUND(SUM(P.PRICE * U.UNITS)/SUM(U.UNITS), 2),
        0
    ) AS AVERAGE_PRICE
FROM PRICES P
LEFT JOIN UNITSSOLD U
ON (
    P.PRODUCT_ID = U.PRODUCT_ID AND
    U.PURCHASE_DATE BETWEEN P.START_DATE AND P.END_DATE
)
GROUP BY 1;

--Problem 1280. Students and Examinations
SELECT S.STUDENT_ID, S.STUDENT_NAME, SU.SUBJECT_NAME,
    (
        SELECT COUNT(1) 
        FROM EXAMINATIONS
        WHERE STUDENT_ID = S.STUDENT_ID
        AND SUBJECT_NAME = SU.SUBJECT_NAME
    ) AS ATTENDED_EXAMS
FROM STUDENTS S
JOIN SUBJECTS SU
ORDER BY S.STUDENT_ID, SU.SUBJECT_NAME; 

--Problem 1327. List the Products Ordered in a Period
SELECT PRODUCT_NAME, SUM(UNIT) AS UNIT
FROM PRODUCTS
INNER JOIN ORDERS
USING (PRODUCT_ID)
WHERE MONTH(ORDER_DATE) = 2 
AND YEAR(ORDER_DATE) = 2020
GROUP BY PRODUCT_NAME
HAVING UNIT >= 100;

--Problem 1484. Group Sold Products By The Date
SELECT SELL_DATE, 
    COUNT(DISTINCT PRODUCT) AS NUM_SOLD, 
    GROUP_CONCAT(
        DISTINCT PRODUCT
        ORDER BY PRODUCT
        SEPARATOR ','
    ) AS PRODUCTS
FROM ACTIVITIES
GROUP BY SELL_DATE
ORDER BY SELL_DATE, PRODUCT;

