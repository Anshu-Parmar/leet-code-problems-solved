--Problem 196. Delete Duplicate Emails
DELETE P 
FROM PERSON P
JOIN (
    SELECT MIN(ID) AS MIN_ID, EMAIL
    FROM PERSON
    GROUP BY EMAIL
) AS MIN_EMAILS
ON P.EMAIL = MIN_EMAILS.EMAIL
WHERE P.ID > MIN_EMAILS.MIN_ID;

--Problem 197. Rising Temperature
