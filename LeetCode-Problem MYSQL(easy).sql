--Problem 1121. Queries Quality and Percentage
SELECT QUERY_NAME, 
    ROUND((SUM(RATING/POSITION)/COUNT(QUERY_NAME)), 2) AS QUALITY,
    ROUND(AVG(CASE WHEN RATING < 3 THEN 1 ELSE 0 END) * 100, 2) AS POOR_QUERY_PERCENTAGE
FROM QUERIES
GROUP BY QUERY_NAME;

